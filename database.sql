1. What is DBMS?
A Database Management System is software used to store, manage, and retrieve data efficiently (e.g., MySQL, Oracle).

2. What is a database?
A structured collection of data stored electronically.

3. What is RDBMS?
Relational DBMS stores data in tables (rows & columns) and uses relationships.

4. Difference between DBMS and RDBMS?

DBMS → No relation, simple storage

RDBMS → Tables + relationships + constraints

5. What is a table?
A collection of rows and columns.
